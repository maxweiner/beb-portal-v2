rjo_norm AS (
  SELECT name AS raw,
         LOWER(BTRIM(
           REGEXP_REPLACE(
             REGEXP_REPLACE(
               REGEXP_REPLACE(name, '[''.,]', '', 'g'),
               E'\\s+(inc|llc|ltd|co|company)\\.?$', '', 'i'),
             E'\\s+-\\s*.{1,40}$', '', 'i')
         )) AS norm
  FROM rjo_raw
),
stores_norm AS (
  SELECT id, name AS raw,
         LOWER(BTRIM(
           REGEXP_REPLACE(
             REGEXP_REPLACE(
               REGEXP_REPLACE(name, '[''.,]', '', 'g'),
               E'\\s+(inc|llc|ltd|co|company)\\.?$', '', 'i'),
             E'\\s+-\\s*.{1,40}$', '', 'i')
         )) AS norm
  FROM public.trunk_show_stores
),
best_match AS (
  SELECT DISTINCT ON (s.id)
    s.id, s.raw AS store_name, n.raw AS rjo_name,
    similarity(s.norm, n.norm) AS sim
  FROM stores_norm s
  CROSS JOIN rjo_norm n
  ORDER BY s.id, similarity(s.norm, n.norm) DESC, n.raw
)
SELECT
  s.name                                       AS store_name,
  b.rjo_name                                   AS best_rjo_match,
  ROUND(b.sim::numeric, 2)                     AS similarity,
  CASE WHEN EXISTS (
    SELECT 1 FROM public.store_trade_organization_members m
    JOIN public.trade_organizations o ON o.id = m.org_id
    WHERE m.store_id = s.id AND o.name = 'RJO'
  ) THEN 'tagged-RJO' ELSE 'untagged' END      AS status
FROM public.trunk_show_stores s
LEFT JOIN best_match b ON b.id = s.id
ORDER BY status DESC, similarity DESC NULLS LAST, store_name;
