-- Normalize both sides identically.
rjo_norm AS (
  SELECT name AS raw,
         LOWER(BTRIM(
           REGEXP_REPLACE(
             REGEXP_REPLACE(
               REGEXP_REPLACE(name, '[''.,]', '', 'g'),
               E'\\s+(inc|llc|ltd|co|company)\\.?$', '', 'i'),
             E'\\s+-\\s*.{1,40}$', '', 'i')
         )) AS norm
  FROM rjo_raw
),
stores_norm AS (
  SELECT id, name AS raw,
         LOWER(BTRIM(
           REGEXP_REPLACE(
             REGEXP_REPLACE(
               REGEXP_REPLACE(name, '[''.,]', '', 'g'),
               E'\\s+(inc|llc|ltd|co|company)\\.?$', '', 'i'),
             E'\\s+-\\s*.{1,40}$', '', 'i')
         )) AS norm
  FROM public.trunk_show_stores
),
-- Best (highest-similarity) RJO match per trunk store, threshold 0.30.
best_match AS (
  SELECT DISTINCT ON (s.id)
    s.id            AS store_id,
    s.raw           AS store_name,
    n.raw           AS rjo_name,
    similarity(s.norm, n.norm) AS sim
  FROM stores_norm s
  CROSS JOIN rjo_norm n
  WHERE similarity(s.norm, n.norm) >= 0.30
  ORDER BY s.id, similarity(s.norm, n.norm) DESC, n.raw
)
INSERT INTO public.store_trade_organization_members (store_id, org_id)
SELECT b.store_id, o.id
FROM best_match b
CROSS JOIN public.trade_organizations o
WHERE o.name = 'RJO'
ON CONFLICT (store_id, org_id) DO NOTHING;

